namespace Aimtec.SDK.Util
{
    /// <summary>
    ///     The keyboard events.
    /// </summary>
    public enum KeyboardEvents
    {
        /// <summary>
        ///     The key down event.
        /// </summary>
        KeybdeventfKeydown = 0x0,

        /// <summary>
        ///     The key up event.
        /// </summary>
        KeybdeventfKeyup = 0x2,

        /// <summary>
        ///     The shift virtual event.
        /// </summary>
        KeybdeventfShiftvirtual = 0x10,

        /// <summary>
        ///     The shift scan code event.
        /// </summary>
        KeybdeventfShiftscancode = 0x2a
    }
}